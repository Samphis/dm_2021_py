def trans_N_Z(mas):
    """
    Функция преобразует натуральное число в целое
    На вход подаётся массив вида [n, A[...]],
    где n - номер старшей позиции, A[...] - массив с цифрами числа
    Возвращает массив вида [b, n, A[...]], где b - знак числа,
    записанного в массиве A[...]
    """
    mas = [0]+mas
    return mas
